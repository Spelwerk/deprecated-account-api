const refreshTokenSQL = require('../refreshToken/refreshTokenSQL');

// ////////////////////////////////////////////////////////////////////////////////// //
// PUBLIC
// ////////////////////////////////////////////////////////////////////////////////// //

const logout = async (req, res, next) => {
    try {
        await refreshTokenSQL.revokeToken(req.tokens.refreshToken.uuid);
        res.status(204).send();
    } catch (err) {
        return next(err);
    }
};

// ////////////////////////////////////////////////////////////////////////////////// //
// EXPORTS
// ////////////////////////////////////////////////////////////////////////////////// //

module.exports = { logout };
